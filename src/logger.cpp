#include "logger.h"

Logger Logger::s_logger;

// =====================================================================================================================
Logger& Logger::get()
{
    return s_logger;
}

// =====================================================================================================================
Logger& Logger::operator<<(const Lock&)
{
    m_mutex.lock();
    return *this;
}

// =====================================================================================================================
Logger& Logger::operator<<(const Unlock&)
{
    m_mutex.unlock();
    return *this;
}
